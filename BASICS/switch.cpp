#include<bits/stdc++.h>
using namespace std;
int main(){
    int day = 3;

    switch(day){
        case 1: 
        cout<< "MONDAY";
        break;

        case 2: 
        cout<< "TUESDAY";
        break;

        case 3: 
        cout<< "WEDNESDAY";
        break;

        case 4: 
        cout<< "THURSDAY";
        break;

        default:
        cout<<"INVALID";


    }

    return 0;
}